#Faça um algoritmo que calcule e mostre a média de três números inteiros.

numero1 = int(input("Informe o 1° número: "))
numero2 = int(input("Informe o 2° número: "))
numero3 = int(input("Informe o 3° número: "))
media = (numero1 + numero2 + numero3)/3
print(f"A média dos três números inteiros informados será {media}")