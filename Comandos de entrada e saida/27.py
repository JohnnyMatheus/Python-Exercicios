'''Elabore um algoritmo que calcule a área de uma sala retangular.
O usuário deve informar as medidas dos dois lados.'''

base = int(input("Informe o valor da base do retângulo: "))
altura = int(input("informe o valor da altura do restângulo: "))
area = base * altura
print(f"A área de uma sala retângular será: {area} ")