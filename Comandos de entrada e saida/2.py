'''Considere um algoritmo que calcule a media do peso de uma família de 5 pessoas.'''
print("Calculo: média de peso de uma família de 5 pessoas")
p1 = float(input("Informe o 1° peso: "))
p2 = float(input("Informe o 2° peso: "))
p3 = float(input("Informe o 3° peso: "))
p4 = float(input("Informe o 4° peso: "))
p5 = float(input("Informe o 5° peso: "))

media = (p1+p2+p3+p4+p5)/5
print("A media de peso da família é {}".format(media))