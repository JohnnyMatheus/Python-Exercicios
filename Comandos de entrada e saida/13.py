'''Criar um algoritmo que calcule e mostre a área de um triângulo ((base x altura)/2).
Os valores da base e da altura devem ser informados pelo usuário.'''

base = int(input("Informe o valor da Base do Triângulo: "))
altura = int(input("Informe a altura do Triângulo: "))
area = (base * altura)/2
print(f"A area do Triângulo é : {area}")

